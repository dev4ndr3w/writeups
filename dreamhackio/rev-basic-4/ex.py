#!/usr/bin/env python2

inverse = lambda v0 : ((v0>>4)|(v0<<4)&0x0f0)

crypt_flag = [0x24, 0x27, 0x13, 0xC6, 0xC6, 0x13, 0x16, 0xE6, 0x47, 0xF5, 0x26, 0x96, 0x47, 0xF5, 0x46, 0x27, 0x13, 0x26, 0x26, 0xC6, 0x56, 0xF5, 0xC3, 0xC3, 0xF5, 0xE3, 0xE3, 0x00]

decrypt_flag = ""
for i in range(len(crypt_flag)):
    decrypt_flag += chr(inverse(crypt_flag[i]))

print(decrypt_flag)
